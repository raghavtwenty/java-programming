public class WhileLoop {
    public static void main(String[] args) {

        // Variable initialization
        int count = 1;

        // while loop
        while (count <= 5) {
            // until the condition is true the block executes
            System.out.println("Count: " + count);
            count++; // increment count
        }
    }
}
