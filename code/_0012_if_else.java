public class IfElseCondition {
    public static void main(String[] args) {
        int num = -10;

        // If statement true
        if (num > 0) {
            System.out.println("The number is positive.");
        } else {
            // Else executes if the above condition is false
            System.out.println("The number is non-positive.");
        }
    }
}
